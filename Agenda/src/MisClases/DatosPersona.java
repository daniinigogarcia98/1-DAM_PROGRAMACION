package MisClases;

public class DatosPersona {
	//Atributos
	
	String nombre;
	String apellidos;
	String direccion;
	String numFijo;
	String numMovil;
	String eMail;
	
	//Constructor
	
	DatosPersona(String nom,String ape,String dir,String fijo,String movil,String mail){
		nombre=nom;
		apellidos=ape;
		direccion=dir;
		numFijo=fijo;
		numMovil=movil;
		eMail=mail;
		
	}
	
	public String formatoCorto(){
		String resultado;
		resultado=nombre +" ("+numFijo+" , "+numMovil+")";
	}
}
